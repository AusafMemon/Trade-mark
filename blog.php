<script>
    document.addEventListener("DOMContentLoaded", function() {
        const pageButtons = document.querySelectorAll(".btn-page");
        const leftBtn = document.getElementById("leftBtn");
        const rightBtn = document.getElementById("rightBtn");

        let currentPageIndex = 2; // Initially selected page index (0-based, so "3" is index 2)

        function updatePagination() {
            // Update selected button style
            pageButtons.forEach((button, index) => {
                button.classList.toggle("btn-selected", index === currentPageIndex);
            });

            // Disable left button if on the first page
            leftBtn.disabled = currentPageIndex === 0;
            // Disable right button if on the last page
            rightBtn.disabled = currentPageIndex === pageButtons.length - 1;
        }

        // Click event for page number buttons
        pageButtons.forEach((button, index) => {
            button.addEventListener("click", () => {
                currentPageIndex = index;
                updatePagination();
            });
        });

        // Click event for left navigation button
        leftBtn.addEventListener("click", () => {
            if (currentPageIndex > 0) {
                currentPageIndex--;
                updatePagination();
            }
        });

        // Click event for right navigation button
        rightBtn.addEventListener("click", () => {
            if (currentPageIndex < pageButtons.length - 1) {
                currentPageIndex++;
                updatePagination();
            }
        });

        // Initial update to set the state
        updatePagination();
    });
</script>

